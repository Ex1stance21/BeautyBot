async def seed_data():
    """Наповнення БД демо-даними (якщо таблиця порожня)."""
    async with aiosqlite.connect(DB_PATH) as db:
        cursor = await db.execute("SELECT COUNT(*) FROM services")
        count = (await cursor.fetchone())[0]

        if count == 0:
            await db.executemany(
                "INSERT INTO services (name, description, price, duration_minutes, emoji) VALUES (?, ?, ?, ?, ?)",
                SERVICES
            )
            await db.commit()
            print(f"✅ Seed: додано {len(SERVICES)} послуг")
        else:
            print(f"ℹ️ Seed: вже є {count} послуг, пропускаю")
